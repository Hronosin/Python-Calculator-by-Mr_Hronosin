"""
📦 Converters Module
====================

Модуль конвертации единиц измерения.

Содержит функции для преобразования между различными единицами:
- Расстояние: км ↔ м, км ↔ миля
- Температура: °C ↔ °F, °C ↔ K
- Углы: градусы ↔ радианы

Экспорт:
    Функции расстояния: km_to_m, m_to_km, mi_to_km, km_to_mi
    Функции температуры: c_to_f, f_to_c, c_to_k, k_to_c
    Функции углов: deg_to_rad, rad_to_deg
"""

import math


# =============================================================================
# КОНВЕРТАЦИЯ РАССТОЯНИЙ
# =============================================================================

def km_to_m(kilometers):
    """
    Перевести километры в метры
    
    Args:
        kilometers: Длина в км
    
    Returns:
        float: Длина в метрах
    
    Formula: метры = км × 1000
    
    Example:
        >>> km_to_m(5)
        5000.0
    """
    return kilometers * 1000


def m_to_km(meters):
    """
    Перевести метры в километры
    
    Args:
        meters: Длина в м
    
    Returns:
        float: Длина в км
    
    Formula: км = метры / 1000
    
    Example:
        >>> m_to_km(5000)
        5.0
    """
    return meters / 1000


def mi_to_km(miles):
    """
    Перевести мили в километры
    
    1 миля = 1.60934 км
    
    Args:
        miles: Расстояние в милях
    
    Returns:
        float: Расстояние в км
    
    Example:
        >>> mi_to_km(1)
        1.60934
    """
    return miles * 1.60934


def km_to_mi(kilometers):
    """
    Перевести километры в мили
    
    1 км = 0.621371 миль
    
    Args:
        kilometers: Расстояние в км
    
    Returns:
        float: Расстояние в милях
    
    Example:
        >>> km_to_mi(1)
        0.621371
    """
    return kilometers * 0.621371


# =============================================================================
# КОНВЕРТАЦИЯ ТЕМПЕРАТУР
# =============================================================================

def c_to_f(celsius):
    """
    Конвертировать Цельсий → Фаренгейт
    
    Args:
        celsius: Температура в °C
    
    Returns:
        float: Температура в °F
    
    Formula: °F = (°C × 9/5) + 32
    
    Example:
        >>> c_to_f(0)
        32.0
        >>> c_to_f(100)
        212.0
    """
    return (celsius * 9/5) + 32


def f_to_c(fahrenheit):
    """
    Конвертировать Фаренгейт → Цельсий
    
    Args:
        fahrenheit: Температура в °F
    
    Returns:
        float: Температура в °C
    
    Formula: °C = (°F - 32) × 5/9
    
    Example:
        >>> f_to_c(32)
        0.0
        >>> f_to_c(212)
        100.0
    """
    return (fahrenheit - 32) * 5/9


def c_to_k(celsius):
    """
    Конвертировать Цельсий → Кельвин
    
    Абсолютный ноль (0 K) = -273.15 °C
    
    Args:
        celsius: Температура в °C
    
    Returns:
        float: Температура в Кельвинах
    
    Formula: K = °C + 273.15
    
    Example:
        >>> c_to_k(0)
        273.15
        >>> c_to_k(20)
        293.15
    """
    return celsius + 273.15


def k_to_c(kelvin):
    """
    Конвертировать Кельвин → Цельсий
    
    Args:
        kelvin: Температура в K
    
    Returns:
        float: Температура в °C
    
    Formula: °C = K - 273.15
    
    Example:
        >>> k_to_c(273.15)
        0.0
    """
    return kelvin - 273.15


# =============================================================================
# КОНВЕРТАЦИЯ УГЛОВ
# =============================================================================

def deg_to_rad(degrees):
    """
    Конвертировать градусы в радианы
    
    Args:
        degrees: Угол в градусах
    
    Returns:
        float: Угол в радианах
    
    Formula: радианы = градусы × π/180
    
    Example:
        >>> deg_to_rad(180)
        3.14159...
        >>> deg_to_rad(90)
        1.5707...
    """
    return degrees * (math.pi / 180)


def rad_to_deg(radians):
    """
    Конвертировать радианы в градусы
    
    Args:
        radians: Угол в радианах
    
    Returns:
        float: Угол в градусах
    
    Formula: градусы = радианы × 180/π
    
    Example:
        >>> rad_to_deg(math.pi)
        180.0
        >>> rad_to_deg(math.pi/2)
        90.0
    """
    return radians * (180 / math.pi)


