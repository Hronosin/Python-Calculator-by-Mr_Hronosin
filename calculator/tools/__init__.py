class GraphGenerator:
    """
    Генератор графиков функций
    
    Позволяет получить координаты для визуализации функции на указанном диапазоне.
    """
    
    @staticmethod
    def generate_points(expression: str, x_min: float, x_max: float, 
                       points_count: int = 500) -> tuple:
        """
        Сгенерировать точки для графика функции
        
        Args:
            expression: Выражение функции, где переменная - "x"
                       Пример: "sin(x)", "x^2", "1/x"
            
            x_min: Минимальное значение x
            x_max: Максимальное значение x
            points_count: Количество точек для отрисовки (по умолчанию 500)
        
        Returns:
            tuple: (xs, ys) - два списка координат для графика
                   Может содержать None для недопустимых значений функции
        
        Example:
            >>> xs, ys = GraphGenerator.generate_points("sin(x)", 0, 6.28, 100)
            >>> len(xs)
            100
        """
        xs = []
        ys = []
        
        step = (x_max - x_min) / points_count
        
        for i in range(points_count):
            x = x_min + i * step
            xs.append(x)
            
            try:
                # Вычисляем y = f(x)
                y = evaluate(expression, {"x": x})
                
                # Обработка комплексных чисел - берём реальную часть
                if isinstance(y, complex):
                    y = y.real
                
                # Отбрасываем "бесконечные" значения (очень большие)
                if abs(y) > 1e10:
                    ys.append(None)
                else:
                    ys.append(y)
            
            except:
                # Если функция не определена в этой точке - пропускаем
                ys.append(None)
        
        return xs, ys


