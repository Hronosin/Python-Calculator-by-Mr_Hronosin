class Derivative:
    """
    Численное вычисление производной
    
    Используется метод центральной разности (центральная производная):
    f'(x) ≈ (f(x+h) - f(x-h)) / (2h)
    
    Этот метод точнее одностороннего, ошибка O(h²)
    """
    
    @staticmethod
    def calculate(expression: str, x_value: float, h: float = 0.00001) -> float:
        """
        Вычислить производную функции в точке x
        
        Метод: центральная разность
        Формула: f'(x) ≈ (f(x+h) - f(x-h)) / (2h)
        
        Args:
            expression: Выражение функции, переменная "x"
            x_value: Точка, в которой вычисляем производную
            h: Шаг численного дифференцирования (по умолчанию 0.00001)
        
        Returns:
            float: Приближённое значение производной в точке x
        
        Example:
            >>> Derivative.calculate("x^2", 2.0)  # Производная 2x в точке 2
            4.0
            
            >>> Derivative.calculate("sin(x)", 0)  # Производная sin в 0
            1.0
        """
        try:
            # Вычисляем f(x + h)
            f_plus = evaluate(expression, {"x": x_value + h})
            
            # Вычисляем f(x - h)
            f_minus = evaluate(expression, {"x": x_value - h})
            
            # Применяем формулу центральной разности
            derivative = (f_plus - f_minus) / (2 * h)
            
            # Для комплексных чисел берём реальную часть
            if isinstance(derivative, complex):
                derivative = derivative.real
            
            return float(derivative)
        
        except Exception as e:
            raise ValueError(f"Не можемо обчислити похідну: {e}")


