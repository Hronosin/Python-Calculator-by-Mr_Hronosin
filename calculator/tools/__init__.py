def find_root_bisection(expression: str, a: float, b: float, 
                       tolerance: float = 1e-6) -> float:
    """
    Найти корень уравнения f(x) = 0 методом бисекции
    
    ВАЖНО: f(a) и f(b) должны иметь разные знаки!
    
    Метод: Бисекция (деление отрезка пополам)
    Принцип: Если функция меняет знак на отрезке [a,b], 
             то корень точно находится между a и b
    
    Args:
        expression: Уравнение f(x), где нам нужно найти где f(x) = 0
        a: Левая граница поиска
        b: Правая граница поиска
        tolerance: Точность поиска (по умолчанию 0.000001)
    
    Returns:
        float: Приближённое значение корня
    
    Raises:
        ValueError: Если функция не меняет знак на интервале
    
    Example:
        >>> find_root_bisection("x^2 - 4", 1, 3)  # Найти корень x² - 4 = 0
        2.0
    """
    # Вычисляем значения на концах
    f_a = evaluate(expression, {"x": a})
    f_b = evaluate(expression, {"x": b})
    
    # Проверяем, что функция имеет разные знаки
    if f_a * f_b > 0:
        raise ValueError(
            "f(a) та f(b) мають один знак! Корінь не гарантовано знаходиться в [a,b]"
        )
    
    # Итеративно суживаем диапазон
    while (b - a) > tolerance:
        # Берём середину интервала
        c = (a + b) / 2
        f_c = evaluate(expression, {"x": c})
        
        # Выбираем новый интервал половины
        if f_c * f_a < 0:
            # Корень в левой половине
            b = c
        else:
            # Корень в правой половине
            a = c
    
    return (a + b) / 2
