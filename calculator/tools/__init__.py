class Integral:
    """
    Численное интегрирование (вычисление определённого интеграла)
    
    Используется метод трапеций:
    ∫[a,b] f(x)dx ≈ (b-a) * (f(a)/2 + f(a+h) + f(a+2h) + ... + f(b)/2) / n
    
    Где n - количество подинтервалов
    """
    
    @staticmethod
    def calculate(expression: str, a: float, b: float, 
                 intervals: int = 1000) -> float:
        """
        Вычислить определённый интеграл функции
        
        Метод: правило трапеций
        Вычисляет: ∫[a,b] f(x)dx
        
        Args:
            expression: Выражение функции, переменная "x"
            a: Нижний предел интегрирования
            b: Верхний предел интегрирования
            intervals: Количество подинтервалов для приближения (больше = точнее)
        
        Returns:
            float: Приближённое значение определённого интеграла
        
        Example:
            >>> Integral.calculate("1", 0, 5)  # Интеграл 1 от 0 до 5
            5.0
            
            >>> Integral.calculate("x", 0, 10)  # Интеграл x от 0 до 10
            50.0
        """
        try:
            # Проверка корректности пределов
            if a > b:
                raise ValueError("Нижний предел > верхнего")
            
            # Вычисляем размер одного подинтервала
            h = (b - a) / intervals
            
            # Начальное значение (полусумма значений на краях)
            result = 0.5 * (evaluate(expression, {"x": a}) + 
                          evaluate(expression, {"x": b}))
            
            # Суммируем значения функции во внутренних точках
            for i in range(1, intervals):
                x = a + i * h
                result += evaluate(expression, {"x": x})
            
            # Умножаем на шаг сетки
            result *= h
            
            # Для комплексных чисел берём реальную часть
            if isinstance(result, complex):
                result = result.real
            
            return float(result)
        
        except Exception as e:
            raise ValueError(f"Не можемо обчислити інтеграл: {e}")


